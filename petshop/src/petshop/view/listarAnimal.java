    public void preencherTabela() {
        DefaultTableModel tabela = (DefaultTableModel) lanimal.getModel();
        animalDAO adao = new animalDAO();

        for (Animal a : adao.buscarAnimal(id)) {

            tabela.addRow(new Object[]
            {
                a.getId(),  
                a.getTipo(),
                a.getIdade(),
                a.getRaca()
            });

        }

    }

