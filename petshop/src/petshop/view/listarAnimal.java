    private void excluirAnimalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirAnimalActionPerformed
       if (lanimal.getSelectedRow() == -1) 
        {
            JOptionPane.showMessageDialog(null, "Selecione a linha que deseja excluir");
        } 
        
        else 
        {
            int i =  (int) lanimal.getValueAt(lanimal.getSelectedRow(), 0);
            
            animalDAO adao = new animalDAO();
            adao.deletarAnimal((int) i);
            
            listarCliente lc= new listarCliente();
            lc.setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_excluirAnimalActionPerformed

