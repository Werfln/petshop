    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        if (tipo.getText().equals("") || idade.getText().equals("") || raca.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
        } 
        else 
        {
            Animal a = new Animal();
            animalDAO dao = new animalDAO();
            
            a.setTipo(tipo.getText());
            a.setIdade(Integer.parseInt(idade.getText()));
            a.setRaca(raca.getText()); 
            
            int i = dao.cadastrarAnimal(a,id);

            if (i == 0) 
            {
                int j = JOptionPane.showConfirmDialog(null, "Deseja Cadastrar outro animal?", "Cadastro", JOptionPane.YES_NO_OPTION);

                if (j == 0) {
                    tipo.setText(null);
                    idade.setText(null);
                    raca.setText(null);
                } 
                else 
                {
                    funcoes f = new funcoes();
                    f.setVisible(true);

                    dispose();
                }
            }

    }
    }//GEN-LAST:event_cadastrarActionPerformed

